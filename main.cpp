static void ConvertMatrix2Square(std::vector<std::vector<float>>& mat, float invalid_num)
{
    const int32_t rows = mat.size();
    const int32_t cols = mat[0].size();
    if (rows > cols) {
        int32_t num_to_append = rows - cols;
        for (auto& v : mat) {
            v.reserve(v.size() + num_to_append);
            v.insert(v.end(), num_to_append, invalid_num);
        }
    } else if (cols > rows) {
        int32_t num_to_append = cols - rows;
        mat.reserve(mat.size() + num_to_append);
        for (int32_t y = 0; y < num_to_append; y++) {
            mat.push_back(std::vector<float>(cols, invalid_num));
        }
    }
}

static void DisplayMatrix(const std::vector<std::vector<float>>& mat)
{
    const int32_t rows = mat.size();
    const int32_t cols = mat[0].size();
    for (int32_t y = 0; y < rows; y++) {
        for (int32_t x = 0; x < cols; x++) {
            printf("%4.2f ", mat[y][x]);
        }
        printf("\n");
    }
    printf("\n");
}

static void Run(const std::vector<std::vector<float>>& mat)
{
    std::vector<int32_t> assign_for_row(mat.size(), -1);
    std::vector<int32_t> assign_for_col(mat[0].size(), -1);

    HungarianAlgorithm<float> solver(mat);
    solver.Solve(assign_for_row, assign_for_col);

    printf("\nassign_for_row\n");
    for (int32_t i = 0; i < assign_for_row.size(); i++) {
        printf("%d  %d\n", i, assign_for_row[i]);
    }

    printf("\nassign_for_col\n");
    for (int32_t i = 0; i < assign_for_col.size(); i++) {
        printf("%d  %d\n", i, assign_for_col[i]);
    }
}

